    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $purchase = new \App\Purchase;
        $purchase->product_id = $request->get('product');
        $purchase->date =  $request->get('date');
        $purchase->quantity = $request->get('quantity');
        $purchase->supplier = $request->get('supplier');
        $purchase->no_invoice = $request->get('no_invoice');
        $purchase->save();

        $purchase->product->stock += $request->get('quantity');
        $purchase->product->save();

        return redirect('purchases');
    }

